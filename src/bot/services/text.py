"""
Сервис работы с текстами сообщений.

Здесь нет зависимостей от aiogram и Telegram API.
"""


def make_echo_reply(text: str) -> str:
    """
    Готовит текст ответа для эхо-бота.

    Сейчас просто повторяет текст пользователя, но в будущем
    здесь можно добавить дополнительную логику (фильтры, шаблоны и т.п.).
    """
    return text


def make_start_message() -> str:
    """
    Возвращает приветственное сообщение для команды /start.
    """
    return (
        "Привет! Я простой эхо-бот.\n"
        "Напиши мне любое сообщение, и я повторю его обратно."
    )


